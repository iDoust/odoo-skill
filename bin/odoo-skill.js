function main() {
    const args = process.argv.slice(2);
    const command = args[0];
    const restArgs = args.slice(1);

    switch (command) {
        case 'init':
            commandInit(restArgs);
            break;
        case 'create':
            commandCreate(restArgs);
            break;
        case 'agents':
            commandAgents();
            break;
        case 'help':
        case '--help':
        case '-h':
        case undefined:
            commandHelp();
            break;
        default:
            console.error(`❌ Unknown command: "${command}"`);
            commandHelp();
            process.exit(1);
    }
}


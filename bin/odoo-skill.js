function commandInit(args) {
    const aiArg = args.find(a => a.startsWith('--ai='))?.split('=')[1]
        || (args.includes('--ai') ? args[args.indexOf('--ai') + 1] : null)
        || 'all';

    const dest = process.cwd();
    const skillDest = path.join(dest, SKILL_FOLDER);

    printSection('📦 Installing Odoo Skill Knowledge Base');
    console.log(`  Destination: ${skillDest}\n`);

    // Copy knowledge base
    const folders = ['agents', 'examples', 'rules', 'skills', 'templates'];
    const files = ['AGENTS.md', 'README.md', 'SKILL.md'];

    for (const folder of folders) {
        const src = path.join(PACKAGE_ROOT, folder);
        const dst = path.join(skillDest, folder);
        if (fs.existsSync(src)) {
            copyRecursiveSync(src, dst);
            console.log(`  ✅ Copied: ${SKILL_FOLDER}/${folder}/`);
        }
    }
    for (const file of files) {
        const src = path.join(PACKAGE_ROOT, file);
        if (fs.existsSync(src)) {
            fs.mkdirSync(skillDest, { recursive: true });
            fs.copyFileSync(src, path.join(skillDest, file));
            console.log(`  ✅ Copied: ${SKILL_FOLDER}/${file}`);
        }
    }

    // Create AI rules files
    printSection('🤖 Generating AI Rules Files');

    const targets = aiArg === 'all'
        ? Object.keys(AI_RULES)
        : aiArg.split(',').map(s => s.trim()).filter(s => AI_RULES[s]);

    for (const ai of targets) {
        const rule = AI_RULES[ai];
        if (!rule) {
            console.log(`  ⚠️  Unknown AI: ${ai}. Valid: ${Object.keys(AI_RULES).join(', ')}, all`);
            continue;
        }
        writeFile(path.join(dest, rule.file), rule.content);
    }

    // Print next steps
    console.log('\n' + '═'.repeat(50));
    console.log('  ✅ odoo-skill installed successfully!');
    console.log('═'.repeat(50));
    console.log('\n💡 How to use:');
    console.log('  1. Open your Odoo project in your AI IDE (Cursor, Windsurf, etc.)');
    console.log('  2. The AI will automatically read the .odoo-skill/ rules');
    console.log('  3. Ask the AI: "Plan a new Odoo module for [your requirement]"');
    console.log('\n📚 Available agent commands to ask your AI:');
    console.log('  • "Plan this module" → loads agents/odoo-planner.md');
    console.log('  • "Review this code" → loads agents/odoo-code-reviewer.md');
    console.log('  • "Debug this error: [traceback]" → loads agents/odoo-debugger.md');
    console.log('  • "Migrate this module to v18" → loads agents/odoo-migrator.md\n');
}

